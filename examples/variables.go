package main

import "fmt"

func main() {
  var a = "Start with vars"
  var b, c = 1, 2
  var d = true
  var e int

  f := "Then use :="
  g, h := 3, 4
  i := false
  var j bool
  var k string

  fmt.Println(a)
  fmt.Println(b, c)
  fmt.Println(d)
  fmt.Println(e)

  fmt.Println(f)
  fmt.Println(g, h)
  fmt.Println(i)
  fmt.Println(j)
  fmt.Println(k)
}
